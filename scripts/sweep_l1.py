#!/usr/bin/env python3
"""L1 fmax-vs-PE-array-size sweep driver (issue #11, PLAN §7 L1 + §3 LV1/LV4).

Compiles the l1_sweep Quartus project across the grid

    NUM_ROWS x NUM_COLS  x  {RETIME_CLEAN, RESET_HEAVY}  x  {MERGED, ISOLATED}

generating one .qsf per grid point, running `quartus_sh --flow compile` (inside the
alterafpga/quartus-pro:26.1-agilex3 container, exactly as scripts/env.sh does), then parsing each
revision's Timing-Analyzer and Fitter reports into a `level: "L1"`, `kind: "estimate"` result JSON
(fmax is a static-timing estimate until a hardware run, per PLAN §7 L0/L1). Finally regenerates
results/reports/l1_curve.md.

Because #9 proved Agilex 3 tensor mode is unreachable from hand-written RTL on Quartus 26.1
(PLAN §3 LV2 caveat), the reused PE cell is classic-mode; this sweep therefore characterises the
CLASSIC-mode fmax curve. `scripts/audit_tensor_mode.py` is run on every point to confirm the DSP
mode is *consistently* classic across the whole grid (a point that silently changed mode would
invalidate the curve — the L1 analogue of #9's tensor-audit gate).

Examples:
    scripts/sweep_l1.py                       # full 24-point grid
    scripts/sweep_l1.py --revisions l1_r3x3_clean_merged l1_r5x5_heavy_isolated
    scripts/sweep_l1.py --skip-compile        # just re-parse existing reports + regenerate JSONs
    scripts/sweep_l1.py --jobs 2              # two concurrent compiles
"""
from __future__ import annotations

import argparse
import concurrent.futures
import json
import re
import subprocess
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
import audit_tensor_mode  # noqa: E402  (reuse its DSP-mode parser)

REPO = Path(__file__).resolve().parent.parent
PROJ = REPO / "quartus" / "l1_sweep"
RESULTS = REPO / "results"
IMAGE = "alterafpga/quartus-pro:26.1-agilex3"
QUARTUS_VER = "26.1.0 Build 110"

# grid: (rows, cols). Each PE is #9's ~5-DSP 10-lane MAC, so total DSP ≈ 5*rows*cols (the fitter
# packs a little denser). Shapes are roughly square and monotonically larger; array shape co-varies
# with size (documented limitation, like the issue's note that seed sweeps are future refinement).
SIZES = [(1, 2), (2, 2), (2, 3), (3, 3), (4, 4), (5, 5)]
RESET = [("clean", 0), ("heavy", 1)]      # RETIME_CLEAN vs RESET_HEAVY  (LV1)
DOMAIN = [("merged", 0), ("isolated", 1)]  # MERGED vs ISOLATED           (LV4)

QSF_FILES = """\
set_global_assignment -name FAMILY "Agilex 3"
set_global_assignment -name DEVICE A3CY100BM16AE7S
set_global_assignment -name TOP_LEVEL_ENTITY l1_sweep_top
set_global_assignment -name LAST_QUARTUS_VERSION "26.1.0 Pro Edition"
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/common/bench_pkg.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/common/cdc_bit_sync.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/common/pulse_sync.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/common/async_fifo.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l0_tensor_chain/l0_lfsr.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l0_tensor_chain/l0_mac_block.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l1_pe_array/l1_pe_cell.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l1_pe_array/l1_pe_array.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l1_pe_array/l1_pe_core.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l1_pe_array/l1_pe_top.sv
set_global_assignment -name SYSTEMVERILOG_FILE l1_sweep_top.sv
"""

QSF_PINS = """\
set_instance_assignment -name IO_STANDARD "1.2 V" -to clk
set_instance_assignment -name IO_STANDARD "1.2 V" -to clk_hot
set_instance_assignment -name IO_STANDARD "1.2 V" -to rst_n
set_instance_assignment -name IO_STANDARD "1.2 V" -to obs[*]
"""


def rev_name(rows: int, cols: int, rlabel: str, dlabel: str) -> str:
    return f"l1_r{rows}x{cols}_{rlabel}_{dlabel}"


def grid():
    for rows, cols in SIZES:
        for rlabel, heavy in RESET:
            for dlabel, iso in DOMAIN:
                yield rows, cols, rlabel, heavy, dlabel, iso


def write_qpf() -> None:
    """(Re)write l1_sweep.qpf listing the full grid, so `quartus_sh -c <rev>` works for any point.
    The .qpf and per-revision .qsf are derived artifacts (gitignored) — generated here."""
    revs = [rev_name(r, c, rl, dl) for r, c in SIZES for rl, _ in RESET for dl, _ in DOMAIN]
    body = 'QUARTUS_VERSION = "26.1"\n\n# GENERATED by scripts/sweep_l1.py (issue #11).\n'
    body += "\n".join(f'PROJECT_REVISION = "{rev}"' for rev in revs) + "\n"
    (PROJ / "l1_sweep.qpf").write_text(body)


def write_qsf(rows, cols, rlabel, heavy, dlabel, iso) -> str:
    rev = rev_name(rows, cols, rlabel, dlabel)
    sdc = "l1_sweep_isolated.sdc" if iso else "l1_sweep_merged.sdc"
    text = (
        f"# GENERATED by scripts/sweep_l1.py — L1 sweep revision {rev} (issue #11).\n"
        f"# {rows}x{cols} PE grid, {'RESET_HEAVY' if heavy else 'RETIME_CLEAN'}, "
        f"{'ISOLATED' if iso else 'MERGED'}.\n"
        + QSF_FILES
        + f"set_global_assignment -name SDC_FILE ../constraints/{sdc}\n"
        + f"set_parameter -name NUM_ROWS {rows}\n"
        + f"set_parameter -name NUM_COLS {cols}\n"
        + f"set_parameter -name RESET_HEAVY {heavy}\n"
        + f"set_parameter -name ISOLATE {iso}\n"
        + QSF_PINS
    )
    (PROJ / f"{rev}.qsf").write_text(text)
    return rev


def compile_rev(rev: str, uid: str, gid: str) -> tuple[str, int, str]:
    """Run one revision compile inside the Quartus container. Returns (rev, rc, tail)."""
    cmd = [
        "docker", "run", "--rm", "-i",
        "--user", f"{uid}:{gid}", "-e", "HOME=/tmp",
        "-v", f"{REPO}:/workspace",
        "-v", "/dev/null:/usr/lib/x86_64-linux-gnu/libudev.so.1",
        "-w", "/workspace/quartus/l1_sweep",
        IMAGE,
        "quartus_sh", "--flow", "compile", "l1_sweep", "-c", rev,
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True)
    tail = "\n".join((proc.stdout + proc.stderr).splitlines()[-4:])
    return rev, proc.returncode, tail


# ---------------- report parsing ----------------
_FMAX_ROW = re.compile(
    r";\s*([\d.]+)\s*MHz\s*;\s*([\d.]+)\s*MHz\s*;\s*(\w+)\s*;", re.IGNORECASE
)


def parse_fmax(sta_path: Path, target_clk: str) -> tuple[float | None, dict]:
    """Return (worst-case restricted fmax for target_clk over all corners, per-clock detail)."""
    if not sta_path.exists():
        return None, {}
    text = sta_path.read_text(errors="replace")
    per_clock: dict[str, list[float]] = {}
    for m in _FMAX_ROW.finditer(text):
        fmax, restricted, clk = float(m.group(1)), float(m.group(2)), m.group(3)
        per_clock.setdefault(clk, []).append(restricted)
    detail = {c: min(v) for c, v in per_clock.items()}
    return (min(per_clock[target_clk]) if target_clk in per_clock else None), detail


_ALM = re.compile(r"Logic utilization \(in ALMs\)\s*;\s*([\d,]+)\s*/\s*([\d,]+)", re.IGNORECASE)
_ALM2 = re.compile(r";\s*ALMs? needed.*?;\s*([\d,]+)", re.IGNORECASE)
_M20K = re.compile(r";\s*M20K(?:s| blocks)?\s*;\s*([\d,]+)\s*/\s*([\d,]+)", re.IGNORECASE)
_M20K2 = re.compile(r"M20K blocks?\s*;\s*([\d,]+)", re.IGNORECASE)


def _num(s: str) -> int:
    return int(s.replace(",", ""))


def parse_fit(fit_path: Path) -> dict:
    out: dict = {"dsp_fixed": None, "dsp_tensor": None, "alm": None, "m20k": None}
    if not fit_path.exists():
        return out
    text = fit_path.read_text(errors="replace")
    try:
        bd = audit_tensor_mode.parse_report(text, str(fit_path))
        out["dsp_fixed"] = bd.fixed
        out["dsp_tensor"] = bd.tensor
        out["dsp_total"] = bd.total()
    except Exception:
        pass
    m = _ALM.search(text) or _ALM2.search(text)
    if m:
        out["alm"] = _num(m.group(1))
    m = _M20K.search(text) or _M20K2.search(text)
    if m:
        out["m20k"] = _num(m.group(1))
    return out


_RETIME_ROW = re.compile(r";\s*(Clock Domain [^;]+?|Clock Transfer[^;]*?)\s*;\s*([^;]+?)\s*;")


def parse_fast_forward(rev: str) -> list[str]:
    """Hyper-Retiming (Fast Forward) 'Retiming Limit Summary' reasons from the `.fit.retime.rpt`
    the Agilex Hyperflex flow emits. Returns e.g. ['clk: Insufficient Registers']. Never fabricates
    — returns [] when the report is absent or unparseable."""
    reasons: list[str] = []
    rpt = PROJ / f"{rev}.fit.retime.rpt"
    if not rpt.exists():
        return reasons
    in_limit = False
    for line in rpt.read_text(errors="replace").splitlines():
        if "Retiming Limit Summary" in line:
            in_limit = True
            continue
        if in_limit:
            if line.startswith("Critical Chain") or line.strip().startswith("Info ("):
                break
            m = _RETIME_ROW.search(line)
            if m and m.group(1).startswith("Clock Domain"):
                dom = m.group(1).replace("Clock Domain", "").strip()
                reasons.append(f"{dom}: {m.group(2).strip()}")
    return reasons[:8]


def emit_json(rev, rows, cols, heavy, iso, date, target_clk) -> Path:
    sta = PROJ / f"{rev}.sta.rpt"
    fit = PROJ / f"{rev}.fit.rpt"
    fmax, fmax_detail = parse_fmax(sta, target_clk)
    fitd = parse_fit(fit)
    ff = parse_fast_forward(rev)
    subject = f"pe-array-fmax-{rev[3:]}"  # strip "l1_"
    data = {
        "kind": "estimate",
        "level": "L1",
        "subject": subject,
        "date": date,
        "plan_ref": "§7 L1",
        "config": {
            "device": "A3CY100BM16AE7S",
            "board": "Arrow AXC3000",
            "fclk_mhz": 300,
            "tool_versions": {"quartus": QUARTUS_VER, "python": "3.12.3"},
            "pe_grid": {"rows": rows, "cols": cols, "pe_count": rows * cols, "n_taps": 10},
            "variant": {
                "reset_style": "reset_heavy" if heavy else "retime_clean",
                "clock_domain": "isolated" if iso else "merged",
            },
            "target_clock": target_clk,
            "utilization": {
                "dsp": fitd.get("dsp_fixed"),
                "dsp_tensor_mode": fitd.get("dsp_tensor"),
                "alm": fitd.get("alm"),
                "m20k": fitd.get("m20k"),
            },
            "fmax_all_clocks_mhz": fmax_detail,
            "fast_forward_limit": ff,
            "report_paths": [f"quartus/l1_sweep/{rev}.sta.rpt", f"quartus/l1_sweep/{rev}.fit.rpt",
                             f"quartus/l1_sweep/{rev}.fit.retime.rpt"],
        },
        "metrics": {"fmax_mhz": fmax},
        "notes": (
            f"Quartus Timing-Analyzer static-timing estimate (kind=estimate per issue #11: fmax from "
            f"the timing report until a hardware run), worst corner, restricted fmax of the "
            f"{'hot (clk_hot)' if iso else 'single (clk)'} clock at the 300 MHz aggressive SDC target. "
            f"CLASSIC-mode datapath (dsp_tensor_mode={fitd.get('dsp_tensor')}, 0 expected — Agilex 3 "
            f"tensor mode is unreachable from hand RTL on Quartus 26.1, PLAN §3 LV2 caveat / #9). "
            f"reset_style={'reset_heavy' if heavy else 'retime_clean'} (LV1), "
            f"clock_domain={'isolated' if iso else 'merged'} (LV4). Emitted by scripts/sweep_l1.py."
        ),
    }
    out = RESULTS / f"l1_{subject}_{date.replace('-', '')}.json"
    out.write_text(json.dumps(data, indent=2) + "\n")
    return out


def generate_report(date: str) -> Path:
    """Regenerate results/reports/l1_curve.md from the emitted L1 JSONs."""
    rows_by_variant: dict[tuple[str, str], dict[tuple[int, int], dict]] = {}
    for p in sorted(RESULTS.glob(f"l1_pe-array-fmax-*_{date.replace('-', '')}.json")):
        d = json.loads(p.read_text())
        g = d["config"]["pe_grid"]
        v = d["config"]["variant"]
        key = (v["reset_style"], v["clock_domain"])
        rows_by_variant.setdefault(key, {})[(g["rows"], g["cols"])] = d
    lines = [
        "# L1 — fmax vs PE-array size (issue #11)",
        "",
        "Generated by `scripts/sweep_l1.py` from the `level: \"L1\"` result JSONs. Every fmax is a "
        "Quartus Timing-Analyzer **estimate** (`kind: estimate`), worst corner, restricted fmax of "
        "the hot clock, at the 300 MHz aggressive SDC target — not a silicon measurement.",
        "",
        "**Classic-mode caveat:** Agilex 3 tensor mode is unreachable from hand-written RTL on "
        "Quartus 26.1 (#9, PLAN §3 LV2), so this is the *classic-mode* PE-array fmax curve — the "
        "honest custom-RTL ceiling. `dsp_tensor_mode` is 0 at every point by construction.",
        "",
        "## fmax (MHz) vs grid, per (reset x domain) variant",
        "",
        "| grid (RxC) | PE | ~DSP | clean/merged | heavy/merged | clean/isolated | heavy/isolated |",
        "|---|---|---|---|---|---|---|",
    ]

    def cell(key, size):
        d = rows_by_variant.get(key, {}).get(size)
        if not d:
            return "—"
        f = d["metrics"]["fmax_mhz"]
        return f"{f:.1f}" if isinstance(f, (int, float)) else "—"

    for size in SIZES:
        any_d = next((rows_by_variant.get(k, {}).get(size) for k in rows_by_variant
                      if rows_by_variant.get(k, {}).get(size)), None)
        dsp = any_d["config"]["utilization"]["dsp"] if any_d else "?"
        lines.append(
            f"| {size[0]}x{size[1]} | {size[0]*size[1]} | {dsp} | "
            f"{cell(('retime_clean','merged'), size)} | {cell(('reset_heavy','merged'), size)} | "
            f"{cell(('retime_clean','isolated'), size)} | {cell(('reset_heavy','isolated'), size)} |"
        )
    def fmax_of(key, size):
        d = rows_by_variant.get(key, {}).get(size)
        f = d["metrics"]["fmax_mhz"] if d else None
        return f if isinstance(f, (int, float)) else None

    # LV1 (retime_clean - reset_heavy) and LV4 (isolated - merged) deltas, on the merged/clean legs
    lines += ["", "## Levers quantified (PLAN §3 LV1, LV4)", "",
              "| grid | ~DSP | LV1 Δ (clean−heavy, merged) | LV4 Δ (isolated−merged, clean) |",
              "|---|---|---|---|"]
    for size in SIZES:
        cm = fmax_of(("retime_clean", "merged"), size)
        hm = fmax_of(("reset_heavy", "merged"), size)
        ci = fmax_of(("retime_clean", "isolated"), size)
        any_d = next((rows_by_variant.get(k, {}).get(size) for k in rows_by_variant
                      if rows_by_variant.get(k, {}).get(size)), None)
        dsp = any_d["config"]["utilization"]["dsp"] if any_d else "?"
        lv1 = f"{cm - hm:+.2f} MHz" if (cm is not None and hm is not None) else "—"
        lv4 = f"{ci - cm:+.2f} MHz" if (ci is not None and cm is not None) else "—"
        lines.append(f"| {size[0]}x{size[1]} | {dsp} | {lv1} | {lv4} |")

    # cliff vs the 300 MHz aggressive target
    all_fmax = [f for k in rows_by_variant for f in
                (fmax_of(k, s) for s in SIZES) if f is not None]
    peak = max(all_fmax) if all_fmax else None
    reached_300 = [s for s in SIZES if any((fmax_of(k, s) or 0) >= 300 for k in rows_by_variant)]
    ff = {}
    for k in rows_by_variant:
        for s in SIZES:
            d = rows_by_variant[k].get(s)
            if d:
                for r in d["config"].get("fast_forward_limit", []):
                    ff[r] = ff.get(r, 0) + 1
    lines += [
        "",
        "## Cliff vs the 300 MHz aggressive target (PLAN §2)",
        "",
        f"- Grid points reaching fmax ≥ 300 MHz: **{len(reached_300)} of {len(SIZES)}** "
        f"({'none' if not reached_300 else ', '.join(f'{r}x{c}' for r, c in reached_300)}).",
        f"- Peak fmax across the whole grid: **{peak:.2f} MHz**." if peak else "",
        "- The classic-mode PE array is combinational-depth-bound (one 10-lane dot product + "
        "cascade add per stage), so fmax plateaus ~50 MHz almost independent of size — there is no "
        "fmax-vs-size *cliff* in this range; the array never approaches the 300 MHz target.",
        "",
        "## Fast Forward / Hyper-Retiming limiting reason (all points)",
        "",
    ] + [f"- `{r}` — {n}/{len(SIZES)*len(rows_by_variant)} points" for r, n in sorted(ff.items())] + [
        "",
        "Register-starved: the reused L0 MAC cell is single-cycle combinational, so the Hyper-Retimer "
        "has nothing to move — which is exactly why the LV1 (reset-style) delta is ~0 except at the "
        "smallest, cascade-free 1x2 point. Full interpretation in "
        "`rtl/microbench/l1_pe_array/README.md`.",
        "",
    ]
    out = REPO / "results" / "reports" / "l1_curve.md"
    out.write_text("\n".join(lines))
    return out


def main(argv=None) -> int:
    import os
    ap = argparse.ArgumentParser(description=__doc__.splitlines()[0],
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--revisions", nargs="*", help="subset of revision names (default: full grid)")
    ap.add_argument("--jobs", type=int, default=1, help="concurrent compiles (RAM ~2-3 GB each)")
    ap.add_argument("--skip-compile", action="store_true", help="parse existing reports only")
    ap.add_argument("--date", default="2026-07-05")
    ap.add_argument("--report-only", action="store_true", help="only regenerate l1_curve.md")
    args = ap.parse_args(argv)

    if args.report_only:
        print("wrote", generate_report(args.date))
        return 0

    want = set(args.revisions) if args.revisions else None
    points = [g for g in grid()
              if want is None or rev_name(g[0], g[1], g[2], g[4]) in want]

    # generate the project (.qpf lists the full grid) + per-point .qsf
    write_qpf()
    to_compile = []
    for rows, cols, rlabel, heavy, dlabel, iso in points:
        rev = write_qsf(rows, cols, rlabel, heavy, dlabel, iso)
        to_compile.append((rev, rows, cols, heavy, iso, dlabel))

    if not args.skip_compile:
        uid, gid = str(os.getuid()), str(os.getgid())
        with concurrent.futures.ThreadPoolExecutor(max_workers=args.jobs) as ex:
            futs = {ex.submit(compile_rev, c[0], uid, gid): c[0] for c in to_compile}
            for fut in concurrent.futures.as_completed(futs):
                rev, rc, tail = fut.result()
                print(f"[compile] {rev}: rc={rc}\n   {tail.replace(chr(10), chr(10)+'   ')}")

    # parse + emit JSONs
    for rev, rows, cols, heavy, iso, dlabel in to_compile:
        target_clk = "clk_hot" if iso else "clk"
        out = emit_json(rev, rows, cols, heavy, iso, args.date, target_clk)
        d = json.loads(out.read_text())
        print(f"[json] {out.name}: fmax={d['metrics']['fmax_mhz']} "
              f"dsp={d['config']['utilization']['dsp']} "
              f"tensor={d['config']['utilization']['dsp_tensor_mode']}")

    print("wrote", generate_report(args.date))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
