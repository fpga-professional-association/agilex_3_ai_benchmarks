"""Unit tests for scripts/l2_golden.py (issue #12, PLAN §7 L2).

Pure-logic model, no Quartus/hardware needed (AGENTS.md). Cross-checked bit-for-bit against
sim/microbench/l2_m20k_bw/tb_m20k_bw.sv's embedded expected constants (both were generated by the
same `python l2_golden.py --num-banks 4 --addr-width 4 --k 20 ...` invocations documented in that
TB's header comment) so a regression in either one is caught by the other.
"""
from __future__ import annotations

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
import l2_golden  # noqa: E402


def test_xorshift32_never_zero():
    # the generator is fixed-point at 0; the RTL/model both guard against it so a bank's content
    # (and the checksum XOR-fold) never degenerates by landing on that fixed point.
    s = 0
    seen_zero = False
    for _ in range(1000):
        s = l2_golden.xorshift32_next(s)
        if s == 0:
            seen_zero = True
    assert not seen_zero


def test_bank_seed_differs_per_bank():
    seeds = [l2_golden.bank_seed(b) for b in range(8)]
    assert len(set(seeds)) == len(seeds)


def test_bank_memory_deterministic_and_full_width_state():
    # same call twice -> identical (determinism); a narrower data_width truncates only the STORED
    # word, not the internal 32-bit running state (m20k_bw_bank.sv header comment).
    mem_a = l2_golden.bank_memory(0, depth=16, data_width=32)
    mem_b = l2_golden.bank_memory(0, depth=16, data_width=32)
    assert mem_a == mem_b

    mem_narrow = l2_golden.bank_memory(0, depth=16, data_width=8)
    assert all(0 <= w <= 0xFF for w in mem_narrow)
    # truncated storage must still match the low 8 bits of the full-width sequence (not a
    # differently-evolved 8-bit LFSR) -- proves the running state stays 32-bit internally.
    mem_full = l2_golden.bank_memory(0, depth=16, data_width=32)
    assert mem_narrow == [w & 0xFF for w in mem_full]


# ---- cross-check against sim/microbench/l2_m20k_bw/tb_m20k_bw.sv's embedded constants ----

EXP_BANK_CS = [0x73E65CF1, 0x59D5F3F0, 0x63D7F011, 0xD1E8BEC8]
EXP_AGG = 0x980CE1D8


def test_banked_outreg_matches_tb_fixture():
    r = l2_golden.run(num_banks=4, addr_width=4, k=20, geometry="banked", output_reg=1)
    assert r["cycles"] == 22
    assert r["checksums"] == EXP_BANK_CS
    assert r["agg_checksum"] == EXP_AGG


def test_shared_outreg_matches_tb_fixture():
    r = l2_golden.run(num_banks=4, addr_width=4, k=20, geometry="shared", output_reg=1)
    assert r["cycles"] == 82
    assert r["checksums"] == EXP_BANK_CS
    assert r["agg_checksum"] == EXP_AGG


def test_banked_noreg_matches_tb_fixture():
    r = l2_golden.run(num_banks=4, addr_width=4, k=20, geometry="banked", output_reg=0)
    assert r["cycles"] == 21
    assert r["checksums"] == EXP_BANK_CS
    assert r["agg_checksum"] == EXP_AGG


def test_shared_noreg_matches_tb_fixture():
    r = l2_golden.run(num_banks=4, addr_width=4, k=20, geometry="shared", output_reg=0)
    assert r["cycles"] == 81
    assert r["checksums"] == EXP_BANK_CS
    assert r["agg_checksum"] == EXP_AGG


def test_geometry_never_changes_the_checksum_only_the_cycle_count():
    banked = l2_golden.run(num_banks=6, addr_width=5, k=13, geometry="banked", output_reg=1)
    shared = l2_golden.run(num_banks=6, addr_width=5, k=13, geometry="shared", output_reg=1)
    assert banked["checksums"] == shared["checksums"]
    assert banked["agg_checksum"] == shared["agg_checksum"]
    assert banked["cycles"] < shared["cycles"]
    # shared serializes NUM_BANKS-fold: total_pulses scales by num_banks, cycles track it 1:1
    assert shared["total_pulses"] == 6 * banked["total_pulses"]


def test_output_reg_adds_exactly_one_cycle_of_drain():
    on = l2_golden.run(num_banks=4, addr_width=4, k=10, geometry="banked", output_reg=1)
    off = l2_golden.run(num_banks=4, addr_width=4, k=10, geometry="banked", output_reg=0)
    assert on["cycles"] == off["cycles"] + 1
    assert on["checksums"] == off["checksums"]


def test_k_zero_is_trivial():
    r = l2_golden.run(num_banks=4, addr_width=4, k=0, geometry="banked", output_reg=1)
    assert r["cycles"] == 0
    assert r["agg_checksum"] == 0
    assert all(c == 0 for c in r["checksums"])


def test_address_wraparound_k_greater_than_depth():
    # K=20 > depth=16 in the fixture above already exercises this path; assert explicitly that a
    # bank's checksum after wraparound differs from the checksum of a hypothetical non-wrapping
    # (K<=depth) shorter run, i.e. the extra 4 reads after wraparound really did contribute.
    short = l2_golden.run(num_banks=1, addr_width=4, k=16, geometry="banked", output_reg=1)
    full = l2_golden.run(num_banks=1, addr_width=4, k=20, geometry="banked", output_reg=1)
    assert short["checksums"] != full["checksums"]


def test_invalid_geometry_rejected():
    import pytest
    with pytest.raises(ValueError, match="geometry"):
        l2_golden.run(num_banks=4, addr_width=4, k=10, geometry="bogus", output_reg=1)


def test_theoretical_gbps():
    # PLAN §3 LV3: banks * bytes/port/cycle * fclk
    gbps = l2_golden.theoretical_gbps(num_banks=32, data_width=32, fclk_mhz=300.0)
    assert gbps == 32 * 4 * 300.0 / 1000.0
