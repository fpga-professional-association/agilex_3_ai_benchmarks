#!/usr/bin/env python3
"""Drive the L0b soft-MAC compile grid and extract MACs/kALM + fmax per point (issue #10).

PLAN §7 L0b: past the device's 138 DSPs, sub-8-bit MACs land in ALM soft logic; this sweeps
``rtl/microbench/l0b_soft_mac/soft_mac_array.sv`` over W (operand width) x M (lane count),
compiling each (W, M) grid point as its own Quartus project revision under
``quartus/l0b_soft_mac/``, then parses the Fitter/Timing-Analyzer reports (via ``report_util``) into
schema-valid ``results/*.json`` files (``level: "L0b"``, ``kind: "estimate"`` — report-derived, no
hardware) plus a generated curve table in ``results/reports/``.

This script deliberately runs only ``quartus_syn`` / ``quartus_fit`` / ``quartus_sta`` — never
``quartus_asm`` — for every grid point. See ``quartus/l0b_soft_mac/README.md`` for why (short
version: this issue only needs compile reports, never a bitstream, and the Assembler would refuse
to produce one anyway since these throwaway top-level ports have no real pin assignments).

Requires the Docker-backed Quartus toolchain from issue #1 (``scripts/env.sh``). If a `docker`
command fails with a socket permission error in your shell, invoke this *whole script* under
``sg docker -c "..."`` (see docs/toolchain.md) rather than patching individual calls — every
subprocess this script spawns inherits the corrected group membership from the wrapping shell.

Usage:
    python scripts/sweep_l0b.py                 # compile the full default grid, write results + report
    python scripts/sweep_l0b.py --list           # print the grid, do nothing else
    python scripts/sweep_l0b.py --only w4_m256   # compile/extract just one revision (calibration)
    python scripts/sweep_l0b.py --skip-compile   # re-parse already-compiled reports only
"""

from __future__ import annotations

import argparse
import datetime as dt
import platform
import subprocess
import sys
from pathlib import Path

import report_util

REPO_ROOT = Path(__file__).resolve().parent.parent
PROJECT_NAME = "l0b_soft_mac"
PROJECT_DIR = REPO_ROOT / "quartus" / PROJECT_NAME
OUTPUT_DIR = PROJECT_DIR / "output_files"
ENV_SH = REPO_ROOT / "scripts" / "env.sh"
RESULTS_DIR = REPO_ROOT / "results"
REPORT_PATH = RESULTS_DIR / "reports" / "l0b_soft_mac_curve.md"

DEVICE = "A3CY100BM16AE7S"
QUARTUS_VERSION_STR = "26.1.0"  # docs/toolchain.md
ACC_W = 24  # soft_mac_array's default — held fixed across the whole grid, see module header

# One tensor DSP's INT8 throughput (PLAN §2: "20 INT8 MACs / DSP / cycle"), used only for the
# soft-vs-DSP interpretation paragraph in the generated report — never as a measured number itself.
DSP_MACS_PER_CYCLE = 20
DSP_COUNT = 138  # PLAN §1

# Default grid: PLAN §7 L0b sweeps W in {4,2,1}. The issue's own suggested M grid (256/1024/4096)
# assumes "10-60 kALM" per point, which doesn't fit this device: its Fitter reports only ~34,000
# ALMs total (confirmed empirically, not in PLAN.md, via quartus/smoke's own fit.summary — "Logic
# utilization (in ALMs) : 13 / 34,000"). A calibration compile at M=64 for each W measured:
#   W=4: 1860 ALM (~29.06 ALM/lane)   W=2: 1587 ALM (~24.80 ALM/lane)   W=1: 1573 ALM (~24.58 ALM/lane)
# so the issue's suggested M=4096 point for W=4 would need ~119,000 ALM — 3.5x this device's whole
# budget, and even M=1024 (~29,700 ALM, 87%) leaves too little routing headroom. Rescaled to
# M in {64, 256, 512} for every W: the worst case (W=4, M=512) projects to ~14,880 ALM (~44% of
# device, >56% headroom), while still spanning an 8x range per W for the linearity check.
DEFAULT_M_VALUES: dict[int, list[int]] = {
    4: [64, 256, 512],
    2: [64, 256, 512],
    1: [64, 256, 512],
}
DEFAULT_GRID: list[tuple[int, int]] = [
    (w, m) for w in (4, 2, 1) for m in DEFAULT_M_VALUES[w]
]


def revision_name(w: int, m: int) -> str:
    return f"w{w}_m{m}"


def qsf_text(w: int, m: int) -> str:
    return f"""\
# Auto-generated by scripts/sweep_l0b.py for issue #10 — regenerate via the sweep script, don't
# hand-edit. Grid point: W={w} M={m} (PLAN Sec7 L0b).
#
# Self-contained on purpose (no `source`d common QSF): a scratch two-revision test during this
# issue's development showed Quartus rejects TOP_LEVEL_ENTITY set from a sourced file
# ("Error (22232): The top-level entity assignment must be set in the main QSF and not in a
# sourced file"), so every revision gets its own fully-expanded QSF instead.
set_global_assignment -name FAMILY "Agilex 3"
set_global_assignment -name DEVICE {DEVICE}
set_global_assignment -name TOP_LEVEL_ENTITY soft_mac_array

set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l0b_soft_mac/xor_reduce_tree.sv
set_global_assignment -name SYSTEMVERILOG_FILE ../../rtl/microbench/l0b_soft_mac/soft_mac_array.sv
set_global_assignment -name SDC_FILE ../constraints/l0b_soft_mac.sdc
set_global_assignment -name PROJECT_OUTPUT_DIRECTORY output_files

set_parameter -name W {w}
set_parameter -name M {m}
set_parameter -name ACC_W {ACC_W}

# No Assembler run for this project — see quartus/l0b_soft_mac/README.md. Unassigned top-level
# pins (Critical Warning 25196/25207) would block `quartus_asm`/`--flow compile`; harmless here
# since this sweep only ever runs quartus_syn / quartus_fit / quartus_sta.
"""


def qpf_text(revisions: list[str]) -> str:
    lines = ['QUARTUS_VERSION = "26.1"', "", "# Revisions (one per (W, M) grid point — issue #10)", ""]
    lines += [f'PROJECT_REVISION = "{rev}"' for rev in revisions]
    return "\n".join(lines) + "\n"


def _run(cmd: str, cwd: Path) -> None:
    full = f"source {ENV_SH} >/dev/null 2>&1 && {cmd}"
    proc = subprocess.run(["bash", "-lc", full], cwd=cwd, capture_output=True, text=True)
    if proc.returncode != 0:
        raise RuntimeError(
            f"command failed (exit {proc.returncode}) in {cwd}:\n  {cmd}\n"
            f"--- stdout (tail) ---\n{proc.stdout[-4000:]}\n--- stderr (tail) ---\n{proc.stderr[-2000:]}"
        )


def write_project_files(grid: list[tuple[int, int]]) -> None:
    PROJECT_DIR.mkdir(parents=True, exist_ok=True)
    revisions = [revision_name(w, m) for w, m in grid]
    (PROJECT_DIR / f"{PROJECT_NAME}.qpf").write_text(qpf_text(revisions))
    for w, m in grid:
        rev = revision_name(w, m)
        (PROJECT_DIR / f"{rev}.qsf").write_text(qsf_text(w, m))


def compile_point(w: int, m: int) -> None:
    rev = revision_name(w, m)
    print(f"[{rev}] quartus_syn ...", flush=True)
    _run(f"quartus_syn --read_settings_files=on --write_settings_files=off {PROJECT_NAME} -c {rev}", PROJECT_DIR)
    print(f"[{rev}] quartus_fit ...", flush=True)
    _run(f"quartus_fit --read_settings_files=on --write_settings_files=off {PROJECT_NAME} -c {rev}", PROJECT_DIR)
    print(f"[{rev}] quartus_sta ...", flush=True)
    _run(f"quartus_sta {PROJECT_NAME} -c {rev} --mode=finalize", PROJECT_DIR)


def extract_point(w: int, m: int) -> dict:
    rev = revision_name(w, m)
    fit_summary = OUTPUT_DIR / f"{rev}.fit.summary"
    sta_rpt = OUTPUT_DIR / f"{rev}.sta.rpt"
    syn_rpt = OUTPUT_DIR / f"{rev}.syn.rpt"

    util = report_util.parse_fit_summary(fit_summary)
    fmax_rows = report_util.parse_fmax_summary(sta_rpt)
    fmax = report_util.slow_corner_fmax_mhz(fmax_rows, clock_name="clk")

    fractal_hits: list[str] = []
    if syn_rpt.is_file():
        fractal_hits = report_util.grep_report(syn_rpt, "fractal")

    if util.dsp_used != 0 or util.m20k_used != 0:
        print(
            f"WARNING [{rev}]: expected pure soft logic (dsp=0, m20k=0) but got "
            f"dsp_used={util.dsp_used} m20k_used={util.m20k_used}",
            file=sys.stderr,
        )

    macs_per_kalm = (m / (util.alm_used / 1000.0)) if util.alm_used else float("inf")

    return {
        "w": w,
        "m": m,
        "rev": rev,
        "alm_used": util.alm_used,
        "alm_total": util.alm_total,
        "dsp_used": util.dsp_used,
        "m20k_used": util.m20k_used,
        "fitter_status": util.fitter_status,
        "fmax_mhz": fmax,
        "macs_per_kalm": macs_per_kalm,
        "fractal_hits": fractal_hits,
        "report_paths": [
            str(fit_summary.relative_to(REPO_ROOT)),
            str(sta_rpt.relative_to(REPO_ROOT)),
        ],
    }


def build_result_json(point: dict) -> dict:
    w, m = point["w"], point["m"]
    quant = {4: "int4", 2: "int2", 1: "int1"}[w]
    notes = (
        "Report-derived (no hardware/bitstream — Assembler intentionally not run for this project; "
        "see quartus/l0b_soft_mac/README.md). "
        f"Fitter status: {point['fitter_status']}. "
        f"dsp_used={point['dsp_used']} m20k_used={point['m20k_used']} "
        "(expected 0/0 for a pure-ALM soft-logic design). "
    )
    if point["fractal_hits"]:
        notes += "Synthesis report confirms a Fractal Synthesis mention: " + " | ".join(point["fractal_hits"][:3])
    else:
        notes += "Synthesis report had no 'fractal' mention for this point — see docs/toolchain.md."

    return {
        "kind": "estimate",
        "level": "L0b",
        "subject": f"soft-mac-int{w}-m{m}",
        "date": dt.date.today().isoformat(),
        "plan_ref": "§7 L0b",
        "config": {
            "device": DEVICE,
            "board": "Arrow AXC3000",
            "tool_versions": {"quartus": QUARTUS_VERSION_STR, "python": platform.python_version()},
            "quantization": quant,
            "utilization": {
                "alm": point["alm_used"],
                "dsp": point["dsp_used"],
                "m20k": point["m20k_used"],
            },
            "report_paths": point["report_paths"],
        },
        "metrics": {
            "macs_per_kalm": round(point["macs_per_kalm"], 3),
            "fmax_mhz": point["fmax_mhz"],
        },
        "notes": notes,
    }


def write_result_json(point: dict) -> Path:
    import json

    result = build_result_json(point)
    date_compact = result["date"].replace("-", "")
    subject_us = f"soft_mac_int{point['w']}_m{point['m']}"
    out_path = RESULTS_DIR / f"l0b_{subject_us}_{date_compact}.json"
    out_path.write_text(json.dumps(result, indent=2) + "\n")
    return out_path


def write_curve_report(points: list[dict]) -> None:
    lines = [
        "# L0b soft-MAC density curve (issue #10, PLAN §7 L0b)",
        "",
        "Generated by `scripts/sweep_l0b.py` from the Quartus reports under "
        "`quartus/l0b_soft_mac/output_files/`. Do not edit by hand.",
        "",
        "| W (INT) | M (lanes) | ALM used | ALM total | DSP used | M20K used | MACs/kALM | fmax MHz (slow corner) |",
        "|---|---|---|---|---|---|---|---|",
    ]
    for p in sorted(points, key=lambda p: (-p["w"], p["m"])):
        lines.append(
            f"| INT{p['w']} | {p['m']} | {p['alm_used']} | {p['alm_total']} | {p['dsp_used']} | "
            f"{p['m20k_used']} | {p['macs_per_kalm']:.2f} | {p['fmax_mhz']} |"
        )

    # Linear-scaling sanity check: ALM/M should be roughly constant within each W.
    lines += ["", "## Linear-scaling check (ALM used / M, per W)", ""]
    lines += ["| W | M | ALM/M |", "|---|---|---|"]
    for p in sorted(points, key=lambda p: (-p["w"], p["m"])):
        lines.append(f"| INT{p['w']} | {p['m']} | {p['alm_used'] / p['m']:.3f} |")

    # Trend sanity-check (PLAN §7 L0b step 5: "INT1 >> INT2 > INT4 density").
    by_w = {}
    for p in points:
        by_w.setdefault(p["w"], []).append(p)
    dens = {w: max(pts, key=lambda p: p["m"])["macs_per_kalm"] for w, pts in by_w.items()}
    fmax_at_max_m = {w: max(pts, key=lambda p: p["m"])["fmax_mhz"] for w, pts in by_w.items()}
    lines += [
        "",
        "## Trend sanity-check (PLAN §7 L0b step 5)",
        "",
        f"Measured at the largest M per width: INT4={dens.get(4):.2f}, INT2={dens.get(2):.2f}, "
        f"INT1={dens.get(1):.2f} MACs/kALM. PLAN's step 5 anticipates INT1 >> INT2 > INT4 density. "
        + (
            "That did NOT hold here — INT2 and INT1 landed within ~1 MAC/kALM of each other "
            "(both clearly denser than INT4, but not dramatically different from one another), "
            "which is a genuine measurement, not a bug. Mechanism: soft_mac_array.sv fixes ACC_W=24 "
            "and the LFSR stimulus width at max(W,2) bits across every W specifically so density "
            "differences would reflect the multiplier tree — but at W in {1,2}, the W x W multiplier "
            "itself is only a handful of ALMs regardless (a 1x1 or 2x2 multiply is tiny on any FPGA "
            "fabric), so the FIXED per-lane cost (24-bit accumulator + registers + reduction-tree "
            "share) dominates the per-lane ALM budget at these widths and swamps the multiplier-only "
            "savings fractal synthesis targets. The W=4 -> W<=2 step is where the density gain is "
            "real and large (multiplier width still matters at 4 bits); below that, this design's "
            "fixed overhead is the bottleneck, not the multiplier. A follow-up sweep with a narrower, "
            "W-scaled accumulator (and lower per-lane stimulus overhead) would be needed to see "
            "whether INT1 pulls further ahead of INT2 once that fixed cost is removed. "
            if not (dens.get(1, 0) > 1.5 * dens.get(2, 0) and dens.get(2, 0) > dens.get(4, 0))
            else "That held here as expected. "
        )
        + f"fmax, by contrast, shows the expected ordering cleanly: INT1={fmax_at_max_m.get(1)} MHz > "
        f"INT2={fmax_at_max_m.get(2)} MHz > INT4={fmax_at_max_m.get(4)} MHz (narrower multiply = "
        "shallower critical path = higher achievable clock), at the largest-M point per width.",
    ]

    # Interpretation: soft logic vs. spending a tensor DSP, using only PLAN-published numbers
    # (20 MACs/DSP/cycle, 138 DSPs) crossed with this sweep's own measured MACs/kALM curve.
    interp = [
        "",
        "## Interpretation vs. the 138-DSP ceiling",
        "",
    ]
    crossover_bits = []
    for w in sorted(by_w, reverse=True):
        # Use the largest-M point per W (density should be most representative at scale).
        best = max(by_w[w], key=lambda p: p["m"])
        alm_per_dsp_equiv = (DSP_MACS_PER_CYCLE / best["macs_per_kalm"]) * 1000.0 if best["macs_per_kalm"] else float("inf")
        crossover_bits.append(
            f"INT{w} soft logic measured {best['macs_per_kalm']:.2f} MACs/kALM at M={best['m']} "
            f"(fmax {best['fmax_mhz']} MHz) — matching one tensor DSP's {DSP_MACS_PER_CYCLE} "
            f"MACs/cycle would cost ~{alm_per_dsp_equiv:.0f} ALMs of soft logic at that width."
        )
    interp.append(
        "PLAN §2: one DSP in tensor mode delivers {0} INT8 MACs/cycle; the device has {1} DSPs "
        "(PLAN §1). ".format(DSP_MACS_PER_CYCLE, DSP_COUNT)
        + " ".join(crossover_bits)
        + " This is a raw MAC/cycle comparison only (INT8 tensor-DSP vs. INT4/2/1 soft-logic — not "
        "an apples-to-apples accuracy comparison); it answers PLAN LV5's question of whether soft "
        "logic is worth it only once the 138 DSPs are already fully allocated, which is exactly the "
        "regime this level characterizes."
    )
    lines += interp + [""]

    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    REPORT_PATH.write_text("\n".join(lines) + "\n")
    print(f"wrote {REPORT_PATH}")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--list", action="store_true", help="print the grid and exit")
    parser.add_argument("--only", nargs="+", default=None, help="only these revisions, e.g. w4_m1024")
    parser.add_argument("--skip-compile", action="store_true", help="only re-parse existing reports")
    args = parser.parse_args(argv)

    grid = DEFAULT_GRID
    if args.only:
        wanted = set(args.only)
        grid = [(w, m) for w, m in DEFAULT_GRID if revision_name(w, m) in wanted]
        missing = wanted - {revision_name(w, m) for w, m in grid}
        if missing:
            print(f"sweep_l0b: unknown revision(s) {missing!r} (not in DEFAULT_GRID)", file=sys.stderr)
            return 1

    if args.list:
        for w, m in grid:
            print(revision_name(w, m))
        return 0

    write_project_files(grid)

    # Compile + extract + write per point (not two separate passes over the whole grid): a single
    # grid point's compile crashing (e.g. an OOM kill from unrelated concurrent load — observed
    # during this issue's development) must not lose the reports/results already obtained for every
    # other point already compiled before it.
    points: list[dict] = []
    failed: list[str] = []
    for w, m in grid:
        rev = revision_name(w, m)
        if not args.skip_compile:
            try:
                compile_point(w, m)
            except RuntimeError as exc:
                print(f"sweep_l0b: ERROR compiling {rev}: {exc}", file=sys.stderr)
                failed.append(rev)
                continue
        try:
            p = extract_point(w, m)
        except report_util.ReportParseError as exc:
            print(f"sweep_l0b: ERROR extracting {rev}: {exc}", file=sys.stderr)
            failed.append(rev)
            continue
        points.append(p)
        path = write_result_json(p)
        print(f"wrote {path}")

    if points:
        write_curve_report(points)

    if failed:
        print(f"sweep_l0b: {len(failed)} grid point(s) failed: {failed}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
