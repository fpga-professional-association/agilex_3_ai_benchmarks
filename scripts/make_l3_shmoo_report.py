#!/usr/bin/env python3
"""Render results/reports/l3_shmoo.md: HyperBus shmoo table + chosen operating point + sustained-BW
table (issue #14, PLAN §7 L3).

Reads every schema-valid `results/*.json` with `level == "L3"` and a `subject` matching
`hyperbus-shmoo-<N>mhz` (one row per HyperBus clock swept, emitted by `sw/host/run_l3.py
shmoo-point`) or `hyperbus-sustained-<N>b` (one row per burst length at the chosen operating point,
emitted by `sw/host/run_l3.py sustain`).

Operating-point selection follows the issue's own rule (PLAN §7 L3 step 3): walk the shmoo rows in
ascending clock order; the first row that is not clean (window invalid, or a nonzero error rate)
marks the first unstable point, and every row at or above that clock is excluded from being the
operating point even if a JSON for it exists (a stray/partial re-run must not silently look stable).
The operating point is the highest clean clock at or below that boundary whose window also has
margin (`window_width_taps >= OPERATING_POINT_MIN_MARGIN`). If no clean row meets the margin, the
report says so explicitly rather than guessing one.

Usage:
    python scripts/make_l3_shmoo_report.py [--out results/reports/l3_shmoo.md] [--check]

--check exits nonzero if the on-disk report differs from freshly generated output (for CI).
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path
from typing import Any

import reslib

OPERATING_POINT_MIN_MARGIN = 3   # PLAN §7 L3 step 3: "operating point ... window >= 3 taps"
ACCEPT_MIN_WINDOW = 2            # PLAN §7 L3 step 1: "window must be >= 2 taps wide to accept"

SHMOO_RE = re.compile(r"^hyperbus-shmoo-([0-9.]+)mhz$")
SUSTAIN_RE = re.compile(r"^hyperbus-sustained-(\d+)b$")


def _is_l3(data: dict[str, Any]) -> bool:
    return data.get("level") == "L3"


def _shmoo_rows(results: list[reslib.ResultFile]) -> list[dict[str, Any]]:
    rows = []
    for r in results:
        d = r.data
        if not _is_l3(d):
            continue
        m = SHMOO_RE.match(d.get("subject", ""))
        if not m:
            continue
        metrics = d.get("metrics", {})
        rows.append({
            "hyperbus_mhz": float(m.group(1)),
            "window_width_taps": metrics.get("window_width_taps"),
            "window_center_tap": metrics.get("window_center_tap"),
            "window_valid": bool(metrics.get("window_valid")),
            "error_rate": metrics.get("error_rate"),
            "err_count": metrics.get("err_count"),
            "n_records": metrics.get("n_records"),
            "date": d.get("date"),
            "path": r,
        })
    rows.sort(key=lambda x: x["hyperbus_mhz"])
    return rows


def _sustain_rows(results: list[reslib.ResultFile]) -> list[dict[str, Any]]:
    rows = []
    for r in results:
        d = r.data
        if not _is_l3(d):
            continue
        m = SUSTAIN_RE.match(d.get("subject", ""))
        if not m:
            continue
        metrics = d.get("metrics", {})
        rows.append({
            "burst_bytes": int(m.group(1)),
            "sustained_mbps": metrics.get("sustained_mbps"),
            "efficiency_pct": metrics.get("efficiency_pct"),
            "direction": metrics.get("direction"),
            "hyperbus_mhz": d.get("config", {}).get("hyperbus_mhz"),
            "date": d.get("date"),
            "path": r,
        })
    rows.sort(key=lambda x: x["burst_bytes"])
    return rows


def _row_is_clean(row: dict[str, Any]) -> bool:
    if not row["window_valid"]:
        return False
    er = row["error_rate"]
    if er is None or er != 0:
        return False
    return True


def choose_operating_point(shmoo_rows: list[dict[str, Any]]) -> dict[str, Any] | None:
    """Highest clean clock at/below the first unstable point, with window margin. None if none
    qualifies."""
    best = None
    for row in shmoo_rows:
        if not _row_is_clean(row):
            break  # first unstable point -- stop (PLAN §7 L3 step 3)
        width = row["window_width_taps"]
        if width is not None and width >= OPERATING_POINT_MIN_MARGIN:
            best = row  # keep advancing -- rows are in ascending clock order
    return best


def _fmt(v: Any) -> str:
    if v is None:
        return "—"
    if isinstance(v, bool):
        return "yes" if v else "no"
    if isinstance(v, float):
        if v != v:
            return "—"
        return f"{v:.6g}"
    return str(v)


def build_report(results: list[reslib.ResultFile]) -> str:
    shmoo = _shmoo_rows(results)
    sustain = _sustain_rows(results)
    op = choose_operating_point(shmoo)

    out = [
        "# HyperBus L3 shmoo + sustained bandwidth",
        "",
        "Generated by `scripts/make_l3_shmoo_report.py` from the `results/` JSONs emitted by "
        "`sw/host/run_l3.py` (issue #14, PLAN §7 L3). Do not edit by hand.",
        "",
    ]

    out.append("## Shmoo: HyperBus clock vs. capture window / error rate")
    out.append("")
    if not shmoo:
        out.append("_No shmoo points yet — this issue's acceptance criteria are hardware-gated; "
                    "see the issue #14 PR's Hardware Handoff section._")
    else:
        out.append("| HyperBus MHz | window taps | center tap | window valid | error rate | "
                    "err count | passes | clean | date |")
        out.append("|---|---|---|---|---|---|---|---|---|")
        first_unstable_seen = False
        for row in shmoo:
            clean = _row_is_clean(row) and not first_unstable_seen
            if not _row_is_clean(row):
                first_unstable_seen = True
            out.append(
                f"| {_fmt(row['hyperbus_mhz'])} | {_fmt(row['window_width_taps'])} | "
                f"{_fmt(row['window_center_tap'])} | {_fmt(row['window_valid'])} | "
                f"{_fmt(row['error_rate'])} | {_fmt(row['err_count'])} | "
                f"{_fmt(row['n_records'])} | {'yes' if clean else 'no'} | {_fmt(row['date'])} |")
        out.append("")
        if op is not None:
            out.append(
                f"**Chosen operating point: {_fmt(op['hyperbus_mhz'])} MHz** "
                f"(window {_fmt(op['window_width_taps'])} taps, centered at tap "
                f"{_fmt(op['window_center_tap'])}; margin rule: width >= "
                f"{OPERATING_POINT_MIN_MARGIN} taps, PLAN §7 L3 step 3).")
        else:
            out.append(
                f"**No operating point chosen yet** — no clean shmoo row currently has "
                f"window width >= {OPERATING_POINT_MIN_MARGIN} taps (margin rule) at/below the "
                "first unstable clock. Run more shmoo points, or accept the wider-margin "
                f"criterion cannot yet be met (min accept width is {ACCEPT_MIN_WINDOW} taps, "
                "PLAN §7 L3 step 1).")
    out.append("")

    out.append("## Sustained bandwidth per burst length (at the chosen operating point)")
    out.append("")
    if not sustain:
        out.append("_No sustained-BW points yet._")
    else:
        out.append("| burst bytes | direction | HyperBus MHz | sustained MB/s | efficiency % | "
                    "date |")
        out.append("|---|---|---|---|---|---|")
        for row in sustain:
            out.append(
                f"| {_fmt(row['burst_bytes'])} | {_fmt(row['direction'])} | "
                f"{_fmt(row['hyperbus_mhz'])} | {_fmt(row['sustained_mbps'])} | "
                f"{_fmt(row['efficiency_pct'])} | {_fmt(row['date'])} |")
        out.append("")
        out.append("PLAN §4: sustained efficiency on long linear bursts should land 70-85% of "
                    "peak (`2 x f_HB`); AGENTS.md: never quote the 2xf_HB peak itself as a "
                    "sustained number. Below 70% -> investigate controller dead cycles (CA "
                    "overhead per burst, docs/hyperbus.md #14 addendum) before accepting.")
    out.append("")

    return "\n".join(out) + "\n"


def generate(results_dir: Path) -> str:
    """Validate all results, then return the report text (raises on invalid input)."""
    validator = reslib.make_validator()
    results: list[reslib.ResultFile] = []
    for path in reslib.iter_result_paths(results_dir):
        result = reslib.load_result(path)
        errs = reslib.validate_result(result.data, validator)
        if errs:
            raise ValueError(f"{path} is invalid; run validate_results.py:\n  " + "\n  ".join(errs))
        results.append(result)
    return build_report(results)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--out", default=str(reslib.RESULTS_DIR / "reports" / "l3_shmoo.md"))
    parser.add_argument("--check", action="store_true",
                        help="exit nonzero if on-disk report differs from generated (CI use)")
    args = parser.parse_args(argv)

    out_path = Path(args.out)
    try:
        text = generate(reslib.RESULTS_DIR)
    except ValueError as exc:
        print(f"cannot build report: {exc}", file=sys.stderr)
        return 1

    if args.check:
        existing = out_path.read_text() if out_path.exists() else ""
        if existing != text:
            print(f"{out_path} is stale — regenerate with: python scripts/make_l3_shmoo_report.py",
                  file=sys.stderr)
            return 1
        print(f"{out_path} up to date")
        return 0

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(text)
    print(f"wrote {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
