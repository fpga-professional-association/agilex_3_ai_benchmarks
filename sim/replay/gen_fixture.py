#!/usr/bin/env python3
"""Generate the replay testbench fixture from the #5 packer (issue #16).

Packs a small deterministic record set, then emits:
  - records.hex        : the packed image as 16-bit little-endian words ($readmemh into the mem BFM)
  - fixture_params.svh : localparams the TB uses (record geometry in words)

Re-run and commit the outputs whenever the record format changes. The TB checks the replayed tensor
words and labels against the memory image itself, so this is a genuine packer-produced fixture.
"""
from __future__ import annotations

import sys
from pathlib import Path

import numpy as np

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE.parents[1] / "sw" / "packer"))
import pack_records  # noqa: E402
import packlib       # noqa: E402

N_BYTES = 490        # KWS-like tensor length (even -> whole words)
COUNT = 8
REC_BASE_WORD = 0


def main() -> int:
    rng = np.random.default_rng(1234)
    inputs = rng.integers(-128, 128, size=(COUNT, N_BYTES), dtype=np.int8)
    labels = rng.integers(0, 12, size=COUNT, dtype=np.int64)
    image, manifest = pack_records.pack(inputs, labels, scale=1.0, zero_point=0, layout="raw",
                                        dataset="replay-fixture")
    assert manifest["n_input_bytes"] == N_BYTES
    stride = manifest["stride"]
    assert stride % 2 == 0 and N_BYTES % 2 == 0

    # image -> 16-bit little-endian words
    if len(image) % 2:
        image += b"\x00"
    words = [image[i] | (image[i + 1] << 8) for i in range(0, len(image), 2)]
    (HERE / "fixtures" / "records.hex").write_text("\n".join(f"{w:04x}" for w in words) + "\n")

    svh = HERE / "fixtures" / "fixture_params.svh"
    svh.write_text(
        "// Auto-generated by sim/replay/gen_fixture.py — do not edit.\n"
        f"localparam int FX_COUNT      = {COUNT};\n"
        f"localparam int FX_N_WORDS    = {N_BYTES // 2};   // tensor words per record\n"
        f"localparam int FX_STRIDE_W   = {stride // 2};   // stride in words\n"
        f"localparam int FX_REC_BASE_W = {REC_BASE_WORD};\n"
        f"localparam int FX_MEM_WORDS  = {len(words)};\n")
    print(f"wrote records.hex ({len(words)} words), fixture_params.svh "
          f"(count={COUNT}, nwords={N_BYTES//2}, stride_w={stride//2})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
