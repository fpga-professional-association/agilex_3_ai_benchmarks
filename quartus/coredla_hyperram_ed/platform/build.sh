#!/usr/bin/env bash
# quartus/coredla_hyperram_ed/platform/build.sh — Track P: AGX3-JTAG -> AXC3000+HyperRAM platform.
#
# Reproduces (from the tracked source in this directory) the CoreDLA hostless-JTAG example design
# retargeted from the vendor's Agilex-3C devkit (LPDDR4 EMIF) to the real Arrow AXC3000 board with
# the upgraded third_party/hyperram DDIO PHY. See docs/ph3_integration.md and docs/ph3_submodule.md
# for the full swap narrative; this script only automates the mechanics.
#
# Usage:
#   source scripts/env.sh
#   quartus/coredla_hyperram_ed/platform/build.sh <stage>
# stages (each implies the ones before it):
#   pristine   - dla_build_example_design.py: fetch the vendor agx3c_jtag example design (once;
#                skipped if build/_ed_pristine already exists)
#   overlay    - copy the pristine tree into build/hw/, then overlay this directory's tracked
#                hw/ source files (top.sv, top.qsf, pins.tcl, top.sdc, qsys/ed_zero.tcl, ...) on top
#   qsys       - qsys-script (rebuild shell.qsys from the edited ed_zero.tcl) + qsys-generate -syn
#   syn        - quartus_syn (analysis & synthesis only -- this was Track P's verified milestone:
#                0 errors). Does NOT run quartus_fit/quartus_sta/quartus_asm.
#   compile    - Track B (this session): the full `quartus_sh --flow compile top` -- synthesis +
#                fit + STA + assembler in one shot. This is the stage that (if it succeeds) produces
#                a programmable top.sof under build/hw/output_files/.
#
# Everything under build/ is regenerated output (gitignored, see .gitignore) -- never hand-edit it;
# edit the tracked files in hw/ and re-run this script.
set -euo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$HERE/../../.." && pwd)"
BUILD="$HERE/build"
STAGE="${1:-syn}"

if ! declare -F quartus_sh >/dev/null 2>&1 || ! declare -F dla_build_example_design.py >/dev/null 2>&1; then
    echo "build.sh: source scripts/env.sh first (need quartus_sh/qsys-*/dla_build_example_design.py" \
         "shell functions, see docs/toolchain.md)." >&2
    exit 1
fi

mkdir -p "$BUILD"
cd "$REPO_ROOT"
# Path to $BUILD relative to $REPO_ROOT: the env.sh docker wrappers bind-mount $REPO_ROOT at
# /workspace and map the CURRENT working directory (cwd == $REPO_ROOT here) onto it, but do NOT
# rewrite arbitrary absolute-path arguments -- an absolute host path like "$BUILD/_ed_pristine"
# passed as `-o` doesn't exist inside the container (Track B: hit
# "PermissionError: ... '/home/<user>'" as dla_build_example_design.py's mkdir(parents=True) walked
# up a nonexistent tree). Always pass container-relative (i.e. repo-root-relative) paths instead.
BUILD_REL="${BUILD#"$REPO_ROOT"/}"

# ---- pristine: fetch the vendor agx3c_jtag example design (once) --------------------------------
if [ ! -d "$BUILD/_ed_pristine/hw" ]; then
    echo "[build.sh] dla_build_example_design.py build -> $BUILD/_ed_pristine (agx3c_jtag, AGX3_Performance)"
    # Exact command / provenance: docs/ph3_interfaces.md "Provenance of every quoted signal".
    # --skip-compile: we only want the generated RTL/Tcl/Quartus-project tree, not a vendor compile
    # against the stock LPDDR4 EMIF (which this platform replaces before any compile is attempted).
    # Track B fix #1: this must be UNESCAPED. $COREDLA_ROOT is exported by scripts/env.sh on the HOST
    # (to the same path, /opt/altera/fpga_ai_suite/ubuntu/dla, that the AI Suite container uses), so
    # the host bash expands it to a real absolute path before the arg crosses into
    # `dla_build_example_design.py`'s docker wrapper (_envsh_run_ai_suite), which passes argv
    # through verbatim (no further shell re-interpretation inside the container) -- a literal
    # "\$COREDLA_ROOT" string reaches the Python tool unexpanded and it fails with
    # "Could not find '$COREDLA_ROOT/example_architectures/AGX3_Performance.arch'".
    # Track B fix #2: -o must be repo-root-relative (see BUILD_REL comment above), not the absolute
    # host path $BUILD.
    dla_build_example_design.py build -o "$BUILD_REL/_ed_pristine" -f agx3c_jtag \
        "$COREDLA_ROOT/example_architectures/AGX3_Performance.arch" --skip-compile
fi
[ "$STAGE" = "pristine" ] && exit 0

# ---- overlay: pristine tree + this directory's tracked source ----------------------------------
echo "[build.sh] assembling $BUILD/hw"
rm -rf "$BUILD/hw"
cp -a "$BUILD/_ed_pristine/hw" "$BUILD/hw"
# Drop the LPDDR4-EMIF-era generated artifacts the overlay supersedes (regenerated by qsys stage).
rm -rf "$BUILD/hw/qsys/shell" "$BUILD/hw/qsys/shell.qsys" "$BUILD/hw/qsys/ip/shell"
rm -f  "$BUILD/hw/pin_assignments.sdc" "$BUILD/hw/top.out.sdc"
# Overlay every hand-authored file this Track P session produced.
cp "$HERE/hw/top.sv"          "$BUILD/hw/top.sv"
cp "$HERE/hw/top.qsf"         "$BUILD/hw/top.qsf"
cp "$HERE/hw/pins.tcl"        "$BUILD/hw/pins.tcl"
cp "$HERE/hw/top.sdc"         "$BUILD/hw/top.sdc"
cp "$HERE/hw/qsys/ed_zero.tcl" "$BUILD/hw/qsys/ed_zero.tcl"
[ "$STAGE" = "overlay" ] && exit 0

# ---- qsys: rebuild the Platform Designer system from the edited ed_zero.tcl ---------------------
# Track B fix #3: same absolute-vs-container-relative-path bug as fix #2 above, but for the qsys
# stage's own two path-valued flags. `cd "$BUILD/hw/qsys"` (a HOST path, fine -- that `cd` runs in
# this host bash before the dockerized tool is invoked and env.sh's -w mapping handles the cwd), but
# --new-quartus-project and --search-path are literal argv strings the container cannot resolve as
# absolute host paths (hit the same "Failed to create Quartus Project" / default-device fallback
# symptom as the plain mkdir failure above). Compute them relative to the qsys/ cwd instead.
QSYS_DIR="$BUILD/hw/qsys"
IP_SEARCH_REL="$(realpath -m --relative-to="$QSYS_DIR" "$REPO_ROOT/quartus/ip/axc3000_hyperram_axi4"),\$"
QSYS_SCRATCH_REL="$(realpath -m --relative-to="$QSYS_DIR" "$BUILD/_qsys_scratch")"
echo "[build.sh] qsys-script (ed_zero.tcl -> shell.qsys)"
( cd "$QSYS_DIR" && \
  qsys-script --new-quartus-project="$QSYS_SCRATCH_REL" \
    --search-path="$IP_SEARCH_REL" --cmd="set system_name shell;" --script=ed_zero.tcl )
echo "[build.sh] qsys-generate -syn (RTL for synthesis; must NOT emit a black box for hyperram_0 --" \
     " see docs/ph3_integration.md \"the generic-component trap\")"
( cd "$QSYS_DIR" && \
  qsys-generate -syn --family="Agilex 3" --part=A3CY100BM16AE7S \
    --search-path="$IP_SEARCH_REL" shell.qsys )
[ "$STAGE" = "qsys" ] && exit 0

# ---- syn: analysis & synthesis only (Track P's verified milestone) ------------------------------
echo "[build.sh] quartus_syn top (analysis & synthesis only -- no fit/sta/asm)"
( cd "$BUILD/hw" && quartus_syn --write_settings_files=off top )
echo "[build.sh] done. See $BUILD/hw/output_files/top.syn.rpt / top.syn.summary."
[ "$STAGE" = "syn" ] && exit 0

# ---- compile: full flow (Track B) -- synthesis + fit + STA + assembler --------------------------
echo "[build.sh] quartus_sh --flow compile top (full flow: syn+fit+sta+asm)"
( cd "$BUILD/hw" && quartus_sh --flow compile top )
echo "[build.sh] done. See $BUILD/hw/output_files/top.fit.summary, top.sta.summary, top.asm.rpt," \
     "and (if produced) top.sof."
